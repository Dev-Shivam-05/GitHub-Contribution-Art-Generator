export function getPatternGrid(text: string) {
    const upperText = text.toUpperCase();
    const grid: number[][] = Array(7).fill(0).map(() => []);
    
    for (const char of upperText) {
        const map = fontMap[char] || fontMap[' '];
        for (let row = 0; row < 7; row++) {
            grid[row].push(...(map[row] || [0, 0, 0, 0, 0]));
            grid[row].push(0); // Spacing
        }
    }
    return grid;
}

export interface CommitScheduleItem {
    date: string;
    message: string;
    count: number;
}

export function generateCommitScheduleFromGrid(grid: number[][], startDate: Date, intensity: number): CommitScheduleItem[] {
    const schedule: CommitScheduleItem[] = [];
    const numCols = grid[0].length;

    for (let col = 0; col < numCols; col++) {
        for (let row = 0; row < 7; row++) {
            if (grid[row][col] === 1) {
                const date = new Date(startDate);
                date.setDate(date.getDate() + (col * 7) + row);
                const dateString = date.toISOString().split('T')[0];
                
                for (let i = 0; i < intensity; i++) {
                     schedule.push({
                        date: dateString,
                        message: `Contribution: ${dateString} #${i+1}`,
                        count: 1
                    });
                }
            }
        }
    }
    return schedule;
}